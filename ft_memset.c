#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*a;

	a = (char*)b;
	i = 0;
	while (len > i)
	{
		a[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
